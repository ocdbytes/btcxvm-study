const main = () => {
    console.log(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>");
    console.log("\x1b[33m", banner, "\x1b[0m");
    console.log("[BTC x VM Study Tools]");
    console.log(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>\n");

    const option = process.argv[2];

    const ECPair = ECPairFactory(ecc);
    const keyPair = ECPair.makeRandom();
    console.log("Private Key : ", keyPair.privateKey);
    const { address } = bitcoin.payments.p2pkh({ pubkey: keyPair.publicKey });
    console.log("Address : ", address, "\n");

    switch (option) {
        case "1":
            console.log(">>>> Private Key (hex) : ", keyPair.privateKey.toString('hex'));
            console.log(">>>> Address (hex) : ", Buffer.from(address).toString('hex'));
            return;
        case "2":
            const message = process.argv[3];
            const sig = bitcoinMessage.sign(message, keyPair.privateKey, keyPair.compressed);
            console.log("Private Key (hex) : ", keyPair.privateKey.toString('hex'));
            console.log("Address (hex) : ", Buffer.from(address).toString('hex'));
            console.log("\n>>>> Signature : ", sig);
            console.log(">>>> Signature (hex) : ", sig.toString('hex'));
            return;
        case "--help":
            console.log("Options : ");
            console.log("1. Generate Random Keypair");
            console.log("2. Sign a message");
            return;
        default:
            return;
    }
}

main();